<p><b>ERRORS HANDLING</b></p>
<p>Desde el punto de vista clásico, excepciones y try ..catch en temas posteriores</p>
<p>Hay 5 tipos:</p>
<ul class="lista">
    <li>Compile-time errors: Al compilar el script. No se puede capturar</li>
    <li>Fatal errors: Errores que para la ejecución. No se pueden capturar</li>
    <li>Recoverable errors: Fallos que si se pueden atrapar (excepciones)</li>
    <li>Warnings: Advertencias</li>
    <li>Notices: Naaaaaaaaaa (donde esté una buena @) ;)</li>
</ul>
