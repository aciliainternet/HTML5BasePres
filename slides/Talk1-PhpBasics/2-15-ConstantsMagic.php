<p><b>Magic Constants</b></p>
<p> Como todo lo magic de php __ (dos underscore) </p>
<p> En constantes mágicas: __XXXX__</p>
<p>__LINE__ , __FILE__ , __DIR__ , __FUNCTION__ , __CLASS__ , __TRAIT__(5.4) , __METHOD__ , __NAMESPACE__(5.3)</p>
<p><b>Constantes predefinidas</b></p>
<p>Hay muchas, por ejemplo: PHP_EOL (fin de linea), PHP_INT_MAX (el mayor integer), E_ERROR</p>
<p>Hay que conocerlas..mirárselas: <a href="http://www.php.net/manual/es/reserved.constants.php">http://www.php.net/manual/es/reserved.constants.php</a></p>

