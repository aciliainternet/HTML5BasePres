<p><b>Namespaces</b></p>
<p>Los espacios de nombres en PHP proporcionan una manera para agrupar <b>clases, interfaces, funciones y constantes relacionadas</b></p>
<br />
<p>Como ejemplo, el archivo foo.txt puede existir en los directorios /home/greg y /home/otro, pero no pueden existir dos copias de foo.txt en el mismo directorio. Además, para acceder al archivo foo.txt fuera del directorio /home/greg se debe añadir el nombre del directorio delante del nombre del archivo usando el separador de directorios para obtener /home/greg/foo.txt.</p>
<br />
<p>Un espacio de nombre es un lugar abstracto desde donde acceder, no una clase, ni una función.</p>
